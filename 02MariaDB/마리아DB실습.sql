/*
primary key = oracle의 primary key
auto_increment = oracle의 시퀀스로 자동증가 기능
unsigned = 정수형 컬럼으로 지정하는 경우 음수는 사용하지 않고 양수의 범위만 사용
이때 양수의 범위가 2배로 늘어남
*/

CREATE TABLE tb_int(
	idx INT PRIMARY KEY AUTO_INCREMENT,
	num1 TINYINT UNSIGNED NOT NULL,
	num2 SMALLINT NOT NULL,
	num3 MEDIUMINT DEFAULT '100',
	num4 BIGINT,
	fnum1 FLOAT(10,5) NOT NULL,
	fnum2 DOUBLE(20,10)
	);
	
